#練習：試著在不要寫程式的方法下計算下列運算：

print('1 + 3*2 = ', 1 + 3*2)
print('1 + 3**2 = ', 1 + 3**2)
print('(1 + 3)*2 = ', (1 + 3)*2)

print('1 + 3 / 2 *2 = ', 1 + 3 / 2 *2)
print('1 + 3 // 2 *2 = ', 1 + 3 // 2 *2)
print('1 + 3 / 2 **2 = ', 1 + 3 / 2 **2)
print('1 + 3 // 2 **2 = ', 1 + 3 // 2 **2)

print(int(1 + 3 / 2 *2))
print(float(1 + 3 // 2 **2))